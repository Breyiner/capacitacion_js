// operador de asignacion (=)

let variable = 2;
console.log(variable); // 2


variable += 2; // variable = variable + 2;
console.log(variable); // 4



variable -= 2; // variable = variable - 2;
console.log(variable); // 2



variable *= 2; // variable = variable * 2;
console.log(variable); // 4



variable /= 2; // variable = variable / 2;
console.log(variable); // 2



variable %= 3; // variable = variable % 3;
console.log(variable); // 2




variable **= 3; // variable = variable ** 3;
console.log(variable); // 8